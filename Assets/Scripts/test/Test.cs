using System.Collections;
using System.Collections.Generic;
using UnityEngine;
/// <summary>
/// FileName: Test.cs
/// Author: 胡良云（CloudHu）
/// Corporation: 
/// Description: 
/// DateTime: 4/24/2017
/// </summary>
public class Test : MonoBehaviour {

    #region Public Variables  //公共变量区域
    public GameObject Dragon;
    public AudioClip attack;

    #endregion


    #region Private Variables   //私有变量区域
    private Animator ani;
    private AudioSource As;
    private GameObject Lightning;
    #endregion


    #region MonoBehaviour CallBacks //回调函数区域
    // Use this for initialization
    void Start () {
        ani = transform.GetComponent<Animator>();
        if (this.As == null) this.As = FindObjectOfType<AudioSource>();
    }
	
	// Update is called once per frame
	void Update () {
        if (Input.GetKeyDown(KeyCode.A)) //攻击
        {
            ani.SetTrigger("TH Sword Melee Attack 02"); 
        }

        if (Input.GetKeyDown(KeyCode.C)) //攻击
        {
            ani.SetTrigger("TH Sword Cast Spell");
            if (Lightning == null)
            {
                Lightning = Instantiate(Dragon, transform, false) as GameObject;
            }
            Dragon.SetActive(true);
            PlaySound(attack);
        }

        if (Input.GetKeyDown(KeyCode.W))
        {
            ani.SetBool("TH Sword Idle", false);
            ani.SetBool("Wave Hand", true);
            if (Lightning==null)
            {
                Lightning= Instantiate(Dragon,transform,false) as GameObject;
            }
            Lightning.SetActive(true);
            PlaySound(attack);
        }

        if (Input.GetKeyUp(KeyCode.W))
        {
            ani.SetBool("Wave Hand", false);
            ani.SetBool("TH Sword Idle", true);
            Lightning.SetActive(false);
        }
    }
    #endregion

    #region Public Methods	//公共方法区域


    #endregion

    /// <summary>
    /// 播放音效
    /// </summary>
    /// <param name="ac">声音</param>
    void PlaySound(AudioClip Ac)
    {

        if (Ac != null && !As.isPlaying)
        {
            As.PlayOneShot(Ac);
        }

    }
}
