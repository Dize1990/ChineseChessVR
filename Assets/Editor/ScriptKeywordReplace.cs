using System.Collections;
using System.Collections.Generic;
using UnityEditor;
using UnityEngine;
/// <summary>
/// FileName: ScriptKeywordReplace.cs
/// Author: 胡良云（CloudHu）
/// Corporation: 
/// Description: 这个脚本用于处理C#脚本模板
/// DateTime: 2017年2月27日 15:39:40
/// </summary>
public class ScriptKeywordReplace : UnityEditor.AssetModificationProcessor
{
    #region Public Variables  //公共变量区域


    #endregion


    #region Private Variables   //私有变量区域


    #endregion

    #region MonoBehaviour CallBacks //回调函数区域
    public static void OnWillCreateAsset(string path)
    {
        path = path.Replace(".meta", "");
        int index = path.LastIndexOf(".");
        string file = path.Substring(index);
        if (file != ".cs" && file != ".js" && file != ".boo") return;
        string fileExtension = file;

        index = Application.dataPath.LastIndexOf("Assets");
        path = Application.dataPath.Substring(0, index) + path;
        file = System.IO.File.ReadAllText(path);

        file = file.Replace("#CREATIONDATE#", System.DateTime.Now.ToString("d"));
        file = file.Replace("#PROJECTNAME#", PlayerSettings.productName);
        file = file.Replace("#COMPANYNAME#", PlayerSettings.companyName);
        file = file.Replace("#FILEEXTENSION#", fileExtension);
        file = file.Replace("#DEVNAME#", "胡良云（CloudHu）");

        System.IO.File.WriteAllText(path, file);
        AssetDatabase.Refresh();
    }
    #endregion
}
